// inpExporter.js — Generate a SWMM .inp file from the Network model

class InpExporter {

    pad(v, w) { return String(v).padEnd(w); }

    generateInp(net) {
        const opt = net.options || {};
        // Keep the imported flow unit (CMS ≠ LPS: DWF baselines etc. scale
        // with it) unless the UI unit system no longer matches it.
        const impliedUS = ['CFS', 'GPM', 'MGD'].includes((opt.flowUnits || '').toUpperCase());
        const units = opt.flowUnits && ((net.units === 'US') === impliedUS)
            ? opt.flowUnits.toUpperCase()
            : (net.units === 'US' ? 'CFS' : 'LPS');
        const L = [];

        // Determine REPORT_STEP dynamically based on first RAINGAGE interval, if available
        const gages = net.nodes.filter(n => n.type === 'RAINGAGE');
        const needDefaultGage = !gages.length && net.subcatchments.length > 0;
        
        // Options precedence: value edited in the UI (structured opt.* field)
        // → value imported with the model (opt.raw) → app default. Without the
        // raw layer, imported models silently ran with different numerics
        // (ALLOW_PONDING, FORCE_MAIN_EQUATION, MAX_TRIALS, HEAD_TOLERANCE, …).
        const raw = opt.raw || {};
        const rawOr = (key, dflt) => raw[key] !== undefined ? raw[key] : dflt;

        // REPORT_STEP: an imported model's own value wins; the gage-interval
        // heuristic only applies to networks drawn from scratch in the UI.
        let reportStep = opt.reportStep || '00:15:00';
        if (raw['REPORT_STEP'] === undefined) {
            const gageInterval = gages.length > 0 && gages[0].props.interval;
            if (gageInterval) {
                let parts = String(gageInterval).split(':');
                if (parts.length === 2) {
                    reportStep = `${parts[0].padStart(2, '0')}:${parts[1].padStart(2, '0')}:00`;
                } else if (parts.length === 3) {
                    reportStep = `${parts[0].padStart(2, '0')}:${parts[1].padStart(2, '0')}:${parts[2].padStart(2, '0')}`;
                }
            } else if (needDefaultGage) {
                reportStep = '01:00:00'; // Default RG1 has 1:00 interval
            }
        }

        L.push('[TITLE]');
        L.push(';;Project Title/Notes');
        L.push(net.title || 'Generated by OpenSWMM 3D Web UI');
        L.push('');

        L.push('[OPTIONS]');
        L.push(';;Option             Value');
        const emitted = new Set();
        const emitOpt = (key, value) => {
            emitted.add(key);
            L.push(`${this.pad(key, 20)} ${value}`);
        };
        emitOpt('FLOW_UNITS', units);
        emitOpt('INFILTRATION', opt.infiltration || rawOr('INFILTRATION', 'HORTON'));
        emitOpt('FLOW_ROUTING', opt.flowRouting || rawOr('FLOW_ROUTING', 'KINWAVE'));
        emitOpt('LINK_OFFSETS', rawOr('LINK_OFFSETS', 'DEPTH'));
        emitOpt('MIN_SLOPE', rawOr('MIN_SLOPE', '0'));
        emitOpt('ALLOW_PONDING', rawOr('ALLOW_PONDING', 'NO'));
        emitOpt('SKIP_STEADY_STATE', rawOr('SKIP_STEADY_STATE', 'NO'));
        emitOpt('START_DATE', opt.startDate || rawOr('START_DATE', '01/01/2026'));
        emitOpt('START_TIME', opt.startTime || rawOr('START_TIME', '00:00:00'));
        emitOpt('REPORT_START_DATE', rawOr('REPORT_START_DATE', opt.startDate || '01/01/2026'));
        emitOpt('REPORT_START_TIME', rawOr('REPORT_START_TIME', opt.startTime || '00:00:00'));
        emitOpt('END_DATE', opt.endDate || rawOr('END_DATE', '01/01/2026'));
        emitOpt('END_TIME', opt.endTime || rawOr('END_TIME', '12:00:00'));
        emitOpt('SWEEP_START', rawOr('SWEEP_START', '01/01'));
        emitOpt('SWEEP_END', rawOr('SWEEP_END', '12/31'));
        emitOpt('DRY_DAYS', rawOr('DRY_DAYS', '0'));
        emitOpt('REPORT_STEP', reportStep);
        emitOpt('WET_STEP', opt.wetStep || rawOr('WET_STEP', '00:05:00'));
        emitOpt('DRY_STEP', opt.dryStep || rawOr('DRY_STEP', '01:00:00'));
        emitOpt('ROUTING_STEP', opt.routingStep || rawOr('ROUTING_STEP', '00:00:30'));
        emitOpt('INERTIAL_DAMPING', rawOr('INERTIAL_DAMPING', 'PARTIAL'));
        emitOpt('NORMAL_FLOW_LIMITED', rawOr('NORMAL_FLOW_LIMITED', 'BOTH'));
        emitOpt('FORCE_MAIN_EQUATION', rawOr('FORCE_MAIN_EQUATION', 'H-W'));
        emitOpt('VARIABLE_STEP', rawOr('VARIABLE_STEP', '0.75'));
        emitOpt('LENGTHENING_STEP', rawOr('LENGTHENING_STEP', '0'));
        emitOpt('MIN_SURFAREA', rawOr('MIN_SURFAREA', '12.557'));
        emitOpt('MAX_TRIALS', rawOr('MAX_TRIALS', '8'));
        emitOpt('HEAD_TOLERANCE', rawOr('HEAD_TOLERANCE', '0.005'));
        emitOpt('SYS_FLOW_TOL', rawOr('SYS_FLOW_TOL', '5'));
        emitOpt('LAT_FLOW_TOL', rawOr('LAT_FLOW_TOL', '5'));
        if (opt.nodeContinuity) emitOpt('NODE_CONTINUITY', opt.nodeContinuity);
        if (opt.andersonAccel) emitOpt('ANDERSON_ACCEL', opt.andersonAccel);
        // engine options the app has no field for (MINIMUM_STEP, THREADS,
        // RULE_STEP, SURCHARGE_METHOD, …) pass through unchanged
        for (const [key, value] of Object.entries(raw)) {
            if (!emitted.has(key)) emitOpt(key, value);
        }
        L.push('');

        if (!net.rawSections || !net.rawSections['EVAPORATION']) {
            L.push('[EVAPORATION]');
            L.push(';;Data Source    Parameters');
            L.push('CONSTANT         0.0');
            L.push('DRY_ONLY         NO');
            L.push('');
        }

        if (gages.length || needDefaultGage) {
            L.push('[RAINGAGES]');
            L.push(';;Name           Format    Interval SCF      Source');
            if (needDefaultGage) {
                L.push('RG1              INTENSITY 1:00     1.0      TIMESERIES TS1');
            }
            gages.forEach(g => {
                const p = g.props;
                L.push(`${this.pad(g.id, 16)} ${this.pad(p.format, 9)} ${this.pad(p.interval, 8)} ${this.pad(p.scf, 8)} ${p.sourceType} ${p.sourceName}`);
            });
            L.push('');
        }

        if (opt.rdiiDecay && opt.rdiiDecay.k0 !== undefined) {
            L.push('[RDII_DECAY]');
            L.push(';;k0             kT               Tref');
            L.push(`${this.pad(opt.rdiiDecay.k0, 16)} ${this.pad(opt.rdiiDecay.kT, 16)} ${this.pad(opt.rdiiDecay.tRef, 16)}`);
            L.push('');
        }

        // --- Subcatchments ---
        if (net.subcatchments.length) {
            L.push('[SUBCATCHMENTS]');
            L.push(';;Name           Rain Gage        Outlet           Area     %Imperv  Width    %Slope   CurbLen  SnowPack');
            net.subcatchments.forEach(s => {
                const p = s.props;
                L.push(`${this.pad(s.id, 16)} ${this.pad(p.raingage || 'RG1', 16)} ${this.pad(p.outlet || '*', 16)} ${this.pad(p.area, 8)} ${this.pad(p.imperv, 8)} ${this.pad(p.width, 8)} ${this.pad(p.slope, 8)} ${this.pad(p.curbLen || 0, 8)}`);
            });
            L.push('');
            if (!net.rawSections || !net.rawSections['SUBAREAS']) {
                L.push('[SUBAREAS]');
                L.push(';;Subcatchment   N-Imperv   N-Perv     S-Imperv   S-Perv     PctZero    RouteTo');
                net.subcatchments.forEach(s => {
                    L.push(`${this.pad(s.id, 16)} 0.01       0.1        0.05       0.05       25         OUTLET`);
                });
                L.push('');
            }
            if (!net.rawSections || !net.rawSections['INFILTRATION']) {
                L.push('[INFILTRATION]');
                L.push(';;Subcatchment   MaxRate    MinRate    Decay      DryTime    MaxInfil');
                net.subcatchments.forEach(s => {
                    L.push(`${this.pad(s.id, 16)} 3.0        0.5        4          7          0`);
                });
                L.push('');
            }

        }

        // --- Nodes ---
        const junctions = net.nodes.filter(n => n.type === 'JUNCTION');
        const outfalls = net.nodes.filter(n => n.type === 'OUTFALL');
        const storages = net.nodes.filter(n => n.type === 'STORAGE');
        const dividers = net.nodes.filter(n => n.type === 'DIVIDER');

        if (junctions.length) {
            L.push('[JUNCTIONS]');
            L.push(';;Name           Elevation  MaxDepth   InitDepth  SurDepth   Aponded');
            junctions.forEach(n => {
                const p = n.props;
                L.push(`${this.pad(n.id, 16)} ${this.pad(p.invertEl, 10)} ${this.pad(p.maxDepth, 10)} ${this.pad(p.initDepth, 10)} ${this.pad(p.surDepth, 10)} ${this.pad(p.aponded, 10)}`);
            });
            L.push('');
        }

        if (outfalls.length) {
            L.push('[OUTFALLS]');
            L.push(';;Name           Elevation  Type       Stage Data       Gated');
            outfalls.forEach(n => {
                const p = n.props;
                const stage = (p.outfallType === 'FIXED') ? this.pad(p.stageData || 0, 16) : this.pad('', 16);
                L.push(`${this.pad(n.id, 16)} ${this.pad(p.invertEl, 10)} ${this.pad(p.outfallType || 'FREE', 10)} ${stage} ${p.gated || 'NO'}`);
            });
            L.push('');
        }

        if (storages.length) {
            L.push('[STORAGE]');
            L.push(';;Name           Elev.      MaxDepth   InitDepth  Shape      Curve Name/Params            Fevap');
            storages.forEach(n => {
                const p = n.props;
                const shape = (p.shape || 'FUNCTIONAL').toUpperCase();
                const geom = shape === 'TABULAR'
                    ? this.pad(p.curveName || '*', 26)
                    : `${this.pad(p.coeff, 8)} ${this.pad(p.exponent, 8)} ${this.pad(p.constant, 8)}`;
                L.push(`${this.pad(n.id, 16)} ${this.pad(p.invertEl, 10)} ${this.pad(p.maxDepth, 10)} ${this.pad(p.initDepth, 10)} ${this.pad(shape, 10)} ${geom} ${this.pad(p.surDepth ?? 0, 8)} ${p.fevap ?? 0}`);
            });
            L.push('');
        }

        if (dividers.length) {
            L.push('[DIVIDERS]');
            L.push(';;Name           Elevation  Diverted Link    Type       Parameters');
            dividers.forEach(n => {
                const p = n.props;
                L.push(`${this.pad(n.id, 16)} ${this.pad(p.invertEl, 10)} ${this.pad(p.divertedLink || '*', 16)} ${this.pad(p.dividerType || 'CUTOFF', 10)} ${this.pad(p.param, 10)} ${this.pad(p.maxDepth, 10)} 0          0`);
            });
            L.push('');
        }

        // --- Links ---
        const conduits = net.links.filter(l => l.type === 'CONDUIT');
        const pumps = net.links.filter(l => l.type === 'PUMP');
        const weirs = net.links.filter(l => l.type === 'WEIR');
        const orifices = net.links.filter(l => l.type === 'ORIFICE');
        const outlets = net.links.filter(l => l.type === 'OUTLET');

        if (conduits.length) {
            L.push('[CONDUITS]');
            L.push(';;Name           From Node        To Node          Length     Roughness  InOffset   OutOffset  InitFlow   MaxFlow');
            conduits.forEach(l => {
                const p = l.props;
                L.push(`${this.pad(l.id, 16)} ${this.pad(l.from, 16)} ${this.pad(l.to, 16)} ${this.pad(p.length || 100, 10)} ${this.pad(p.roughness, 10)} ${this.pad(p.inOffset, 10)} ${this.pad(p.outOffset, 10)} ${this.pad(p.initFlow, 10)} ${this.pad(p.maxFlow, 10)}`);
            });
            L.push('');
        }

        if (pumps.length) {
            L.push('[PUMPS]');
            L.push(';;Name           From Node        To Node          Pump Curve       Status   Startup  Shutoff');
            pumps.forEach(l => {
                const p = l.props;
                L.push(`${this.pad(l.id, 16)} ${this.pad(l.from, 16)} ${this.pad(l.to, 16)} ${this.pad(p.pumpCurve || '*', 16)} ${this.pad(p.status || 'ON', 8)} ${this.pad(p.startup, 8)} ${this.pad(p.shutoff, 8)}`);
            });
            L.push('');
        }

        if (weirs.length) {
            L.push('[WEIRS]');
            L.push(';;Name           From Node        To Node          Type         CrestHt    Qcoeff     Gated    EndCon   EndCoeff Surcharge');
            weirs.forEach(l => {
                const p = l.props;
                L.push(`${this.pad(l.id, 16)} ${this.pad(l.from, 16)} ${this.pad(l.to, 16)} ${this.pad(p.weirType || 'TRANSVERSE', 12)} ${this.pad(p.crestHt, 10)} ${this.pad(p.qCoeff, 10)} ${this.pad(p.gated || 'NO', 8)} ${this.pad(p.endCon ?? 0, 8)} ${this.pad(p.endCoeff ?? 0, 8)} ${p.surcharge || 'YES'}`);
            });
            L.push('');
        }

        if (orifices.length) {
            L.push('[ORIFICES]');
            L.push(';;Name           From Node        To Node          Type         Offset     Qcoeff     Gated    CloseTime');
            orifices.forEach(l => {
                const p = l.props;
                L.push(`${this.pad(l.id, 16)} ${this.pad(l.from, 16)} ${this.pad(l.to, 16)} ${this.pad(p.orificeType || 'SIDE', 12)} ${this.pad(p.offset, 10)} ${this.pad(p.qCoeff, 10)} ${this.pad(p.gated || 'NO', 8)} 0`);
            });
            L.push('');
        }

        if (outlets.length) {
            L.push('[OUTLETS]');
            L.push(';;Name           From Node        To Node          Offset     Type             QTable/Qcoeff    Qexpon     Gated');
            outlets.forEach(l => {
                const p = l.props;
                const type = String(p.outletType || 'FUNCTIONAL/DEPTH').toUpperCase();
                const param = type.startsWith('TABULAR')
                    ? `${this.pad(p.curveName || '*', 16)} ${this.pad('', 10)}`
                    : `${this.pad(p.qCoeff ?? 10, 16)} ${this.pad(p.qExpon ?? 0.5, 10)}`;
                L.push(`${this.pad(l.id, 16)} ${this.pad(l.from, 16)} ${this.pad(l.to, 16)} ${this.pad(p.offset ?? 0, 10)} ${this.pad(type, 16)} ${param} ${p.gated || 'NO'}`);
            });
            L.push('');
        }

        // --- Cross sections (conduits, weirs, orifices) ---
        const xsecLinks = [...conduits, ...weirs, ...orifices];
        if (xsecLinks.length) {
            L.push('[XSECTIONS]');
            L.push(';;Link           Shape        Geom1      Geom2      Geom3      Geom4      Barrels    Culvert');
            xsecLinks.forEach(l => {
                const p = l.props;
                L.push(`${this.pad(l.id, 16)} ${this.pad(p.xShape || 'CIRCULAR', 12)} ${this.pad(p.geom1 ?? 1, 10)} ${this.pad(p.geom2 ?? 0, 10)} ${this.pad(p.geom3 ?? 0, 10)} ${this.pad(p.geom4 ?? 0, 10)} ${this.pad(p.barrels || 1, 10)}`);
            });
            L.push('');
        }

        // --- Default timeseries when any raingage references TS1 ---
        if (!net.rawSections || !net.rawSections['TIMESERIES']) {
            const usesTS1 = needDefaultGage || gages.some(g => g.props.sourceType === 'TIMESERIES' && g.props.sourceName === 'TS1');
            if (usesTS1) {
                L.push('[TIMESERIES]');
                L.push(';;Name           Date       Time       Value');
                L.push('TS1                         0:00       10');
                L.push('TS1                         1:00       20');
                L.push('TS1                         2:00       5');
                L.push('TS1                         3:00       0');
                L.push('');
            }
        }

        L.push('[REPORT]');
        L.push(';;Reporting Options');
        L.push('INPUT      NO');
        L.push('CONTROLS   NO');
        // In this engine the selection below also controls the binary .out:
        // the results viewer reads node/link series only, and subcatchment
        // series made the .out ~7x larger on city-scale models.
        L.push('SUBCATCHMENTS NONE');
        L.push('NODES ALL');
        L.push('LINKS ALL');
        
        if (net.rawSections && net.rawSections['REPORT']) {
            // Include user's other report options if any, but ignore standard ones we just set
            const ignores = ['INPUT', 'CONTROLS', 'SUBCATCHMENTS', 'NODES', 'LINKS'];
            net.rawSections['REPORT'].forEach(line => {
                const upper = line.toUpperCase().trim();
                // ONLY allow valid report parameters, don't allow node IDs/coordinates to leak in!
                if (upper && !ignores.some(ig => upper.startsWith(ig))) {
                    // Quick heuristic: a valid report option typically doesn't start with a node name (which won't match standard SWMM options)
                    if (upper.startsWith('CONTINUOUS') || upper.startsWith('AVERAGES') || upper.startsWith('FLOWSTATS') || upper.startsWith('PARAMETERS')) {
                        L.push(line);
                    }
                }
            });
        }
        L.push('');

        // --- Geometry ---
        L.push('[COORDINATES]');
        L.push(';;Node           X-Coord            Y-Coord');
        net.nodes.filter(n => n.type !== 'RAINGAGE').forEach(n => {
            L.push(`${this.pad(n.id, 16)} ${this.pad(n.lngLat[0].toFixed(6), 18)} ${this.pad(n.lngLat[1].toFixed(6), 18)}`);
        });
        L.push('');

        const withVerts = net.links.filter(l => l.vertices && l.vertices.length);
        if (withVerts.length) {
            L.push('[VERTICES]');
            L.push(';;Link           X-Coord            Y-Coord');
            withVerts.forEach(l => {
                l.vertices.forEach(v => {
                    L.push(`${this.pad(l.id, 16)} ${this.pad(v[0].toFixed(6), 18)} ${this.pad(v[1].toFixed(6), 18)}`);
                });
            });
            L.push('');
        }

        if (net.subcatchments.length) {
            L.push('[POLYGONS]');
            L.push(';;Subcatchment   X-Coord            Y-Coord');
            net.subcatchments.forEach(s => {
                s.ring.forEach(pt => {
                    L.push(`${this.pad(s.id, 16)} ${this.pad(pt[0].toFixed(6), 18)} ${this.pad(pt[1].toFixed(6), 18)}`);
                });
            });
            L.push('');
        }

        if (gages.length) {
            L.push('[SYMBOLS]');
            L.push(';;Gage           X-Coord            Y-Coord');
            gages.forEach(g => {
                L.push(`${this.pad(g.id, 16)} ${this.pad(g.lngLat[0].toFixed(6), 18)} ${this.pad(g.lngLat[1].toFixed(6), 18)}`);
            });
            L.push('');
        }


        // --- Append any raw sections that we did not parse explicitly ---
        if (net.rawSections) {
            const handledSections = new Set([
                'TITLE', 'OPTIONS', 'RAINGAGES', 'SUBCATCHMENTS',
                'JUNCTIONS', 'OUTFALLS', 'STORAGE',
                'DIVIDERS', 'CONDUITS', 'PUMPS', 'WEIRS', 'ORIFICES', 'OUTLETS', 'XSECTIONS',
                'COORDINATES', 'VERTICES', 'POLYGONS', 'SYMBOLS', 'REPORT'
            ]);
            for (const [secName, lines] of Object.entries(net.rawSections)) {
                if (!handledSections.has(secName)) {
                    L.push(`[${secName}]`);
                    lines.forEach(line => L.push(line));
                    L.push('');
                }
            }
        }

        return L.join('\n');
    }

    downloadInp(net) {
        const text = this.generateInp(net);
        const blob = new Blob([text], { type: 'text/plain' });
        const a = document.createElement('a');
        a.href = URL.createObjectURL(blob);
        a.download = (net.title.replace(/\s+/g, '_') || 'network') + '.inp';
        a.click();
        URL.revokeObjectURL(a.href);
        return text;
    }
}

window.inpExporter = new InpExporter();
